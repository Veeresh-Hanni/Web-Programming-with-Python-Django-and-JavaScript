# int
a = 12
print(type(a),a)

# float
b = 12.5
print(type(b),b)

# string
c = "Hello, World!"
print(type(c),c)

# boolean
d = True
print(type(d),d)

# list
e = [1, 2, 3, 4, 5]
print(type(e),e)

# tuple
f = (1, 2, 3, 4, 5)
print(type(f),f)

# set
g = {1, 2, 3, 4, 5}
print(type(g),g)

# dictionary
h = {"name": "Alice", "age": 25}
print(type(h),h) 

i = None
print(type(i),i)